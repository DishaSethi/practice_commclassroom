package com.lavs;

public class Whileloop {
    public static void main(String[]args){
//        while loops-use when we dont know how many loops will happen
        /*
        syntax:
        while(condition){
//        body
           increment/decrement
              }

         */
        int num=1;
        while (num<=25){
            System.out.println(num);
            num+=3;
        }
    }
}
